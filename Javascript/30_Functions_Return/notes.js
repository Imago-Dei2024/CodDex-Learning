// ==== Function Syntax ==== // 
// In Javascript, we use the function keyword to define a function with a name and then a set of parentheses. // 
function myFunction() { 
  // Code here // 
} 

myFunction(); 

// We can write the body of the function inside the curly braces {} and that can be anything we have learned so far // 
// After we define a function, we can use it, or "call it" later in our program by using its name with parenthesis; 

// ==== Return ==== // 
// For the most part, functions process our code and produce a result that we log to the console. // 
function helloWorld() { 
  return "Hello, World!"; 
} 
helloWorld(); 
// Calling this function returns the string, but we need to use console.log() to make it appear on the console:  
console.log(helloWorld()); 