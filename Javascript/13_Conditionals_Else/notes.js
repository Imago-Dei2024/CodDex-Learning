// ==== The "else" clause ==== // 

// An "else" clause can optionally be added to the end of an if conditional // 
if (condition) { 
    // Do something
} else { 
    // Do something else
} 
// If the condition is true, execute the code inside the if 
// If the condition is false, execute the code inside the else 

if (grade > 60) { 
    console.log("You Passed!");
} else { 
    console.log("You Failed");
} 

